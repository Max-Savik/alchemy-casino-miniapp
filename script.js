let cdTimer;

function runLocalCountdown(sec){
  clearInterval(cdTimer);
  updateCountdown(sec);
  lockBets(true);                       // –ø–æ–∫–∞ —Ç–∏–∫–∞–µ—Ç ‚Äì —Å—Ç–∞–≤–∫–∏ –∑–∞–ø—Ä–µ—â–µ–Ω—ã
  cdTimer = setInterval(()=>{
    sec--;
    if (sec <= 0){
      clearInterval(cdTimer);
      updateCountdown(0);
    } else {
      updateCountdown(sec);
    }
  }, 1000);
}

